const myclass = document.querySelector(".myclass");

class Shape {
  constructor(width, height, color) {
    this.width = width;
    this.height = height;
    this.color = color;
  }

  draw() {
    console.log(`drawing ${this.color} color of`);
  }

  getArea() {
    return this.width * this.height;
  }
}

shape = new Shape(20, 20, "blue");

myclass.innerText = shape.getArea();
